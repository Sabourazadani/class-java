package Polymorphism;

/**
	Polymorphism means "many forms", and it occurs when we have many classes that are related
	 to each other by inheritance.
	 Types of Polymorphism
	 
	 
	 1. Static - Compile time / Overloading
	 --> it is also known as static polymorphism. This type of polymorphism is achieved by 
	 	 function overloading or operator overloading
	 
	 2. Dynamic - Runtime / Overriding
	 --> It is also known as Dynamic Method Dispatch. It is a process in which a function call 
	 	 to the overridden method is resolved at Runtime. This type of polymorphism is achieved by 
	 	 Method Overriding.
	 	 
	 	 CASTING
	 	 -->UPCASTING
	 	 	Upcasting is the typecasting of a child object to a parent object.
	 	 	
	 	 -->DOWNCASTING
	 	 	Similarly, downcasting means the typecasting of a parent object to a child object
	 	 	
*/