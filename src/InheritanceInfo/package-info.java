package InheritanceInfo;

	/**
	 	What are the types of inheritance?
	 	There are 5 types of inheritance.
			
			1). Single Inheritance : One class is extended by only one class.
			
			2). Multilevel Inheritance : One class is extended by a class and that class in turn 
			    is extended by another class thus forming a chain of inheritance.
			
			3). Hierarchical Inheritance : One class is extended by many classes.
			
			4).Hybrid Inheritance : It is a combination of above types of inheritance.
			
			5). Multiple Inheritance : One class extends more than one classes. 
				(Java does not support multiple inheritance.)
				 
	 */